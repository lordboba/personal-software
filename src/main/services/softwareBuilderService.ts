import fs from "node:fs/promises";
import path from "node:path";
import { execFile } from "node:child_process";
import { promisify } from "node:util";
import { cloneApp } from "./gitService.js";
import { writePatchArtifact } from "./patchArtifactService.js";
import { appsRoot, id, nowIso, sanitizeTitle } from "../utils.js";
import type { CreateSoftwareInput, ManagedApp } from "../../shared/types.js";

const exec = promisify(execFile);

function inferTitle(request: string) {
  const words = request
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .split(/\s+/)
    .filter((word) => word.length > 2)
    .slice(0, 4);
  return sanitizeTitle(words.join("-") || "personal-software");
}

function appSource(title: string, request: string) {
  return `import React from "react";
import { createRoot } from "react-dom/client";
import "./styles.css";

function App() {
  return (
    <main className="app-shell">
      <section className="intro">
        <p>Personal Software Draft</p>
        <h1>${title}</h1>
        <span>Generated from your request</span>
      </section>
      <section className="request">
        <h2>Build Brief</h2>
        <p>${request.replace(/`/g, "'")}</p>
      </section>
    </main>
  );
}

createRoot(document.getElementById("root")!).render(<App />);
`;
}

